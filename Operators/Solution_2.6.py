num_1 = 5
num_2 = 8

print(num_1 < num_2)    #Less than Operator: returns True if the left operand is less than the right

print(num_1 <= num_2)    #Less than or equal to Operator: returns True if left operand is less than or equal to the right

print(num_1 > num_2)     #Greater than Operator: returns True if the left operand is greater than the right
 
print(num_1 >= num_2)   #Greater than or equal to Operator: True if left operand is greater than or equal to the right
