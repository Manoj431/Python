var_i = 5
var_bool = True
var_char = "M"          
var_float = 2.9
var_double = 19.125431

print(type(var_i))          #Returns integer data type.
print(type(var_bool))       #Returns boolean data type.
print(type(var_char))       #Returns string data type because python does not have character data type.
print(type(var_float))      #Returns float data type.
print(type(var_double))     #Returns float because python do not have double data type rather have float data type.

""" In Python we don't have to define the data type of a variable, beacause while assigning
    a value to the variable it automatically assign  the data type to the variable."""